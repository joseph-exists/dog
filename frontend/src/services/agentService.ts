/**
 * Agent scope - determines visibility and ownership
 */
export type AgentScope = "system" | "personal"

/**
 * Agent participation mode - how the agent engages in rooms by default
 * - "always": Agent responds to all messages
 * - "on_mention": Agent responds when mentioned
 * - "manual": Agent only responds when explicitly invoked
 */
export type ParticipationMode = "always" | "on_mention" | "manual"

/**
 * AgentViewModel
 *
 * UI-optimized view of UserAgentConfig. This is NOT the Agent itself -
 * it's the configuration template used to instantiate runtime Agents.
 *
 * Critical Associations:
 * - user_access_provider: UUID reference to UserAccessProvider (the access channel)
 * - provider_type: the API message format
 * - model_name: string (the model identifier)
 *
 * ⚠️  VALIDATION: When using this config to create an Agent, validate:
 *     1. UserAccessProvider exists and is enabled
 *     2. ProviderType matches what UserAccessProvider supports
 *     3. Model is available at UserAccessProvider and supports ProviderType
 *
 * See userAccessProviderService for validation utilities (TODO: implement)
 *
 * Transformations from backend UserAgentConfigPublic:
 * - Provides strict union types for scope and participation_mode
 * - Normalizes nullable fields
 * - Parses ISO timestamp strings to Date objects
 */
export interface AgentViewModel {
  id: string
  name: string
  slug: string
  description: string | null

  // Model Configuration
  model_id: string | null              // Model UUID reference (for catalog models)
  model_name: string                   // Model identifier string (e.g., "gpt-4o-mini")
  provider_type: string       // API specification type (e.g., "openai", "anthropic")
  user_access_provider: string | null  // UUID reference to UserAccessProvider

  // Prompts and Instructions
  system_prompt: string | null         // Base system prompt from agent config
  custom_system_prompt: string | null  // User override for system prompt
  instructions: string | null          // Large text field for detailed instructions

  // Configuration
  tool_config: Record<string, unknown> | null
  deps_config: Record<string, unknown> | null
  agent_metadata: Record<string, unknown> | null

  // Behavior Settings
  is_enabled: boolean
  is_clonable: boolean                 // Whether this config can be cloned by others
  is_visible: boolean                  // Visibility setting
  scope: AgentScope                    // "system" | "personal"
  participation_mode: ParticipationMode // "always" | "on_mention" | "manual"
  is_coordinator: boolean              // Whether agent can coordinate other agents
  max_tool_iterations: number | null   // Maximum tool call iterations
  capabilities: string[]               // Agent capability tags

  // Metadata
  owner_id: string | null
  created_at: Date
  updated_at: Date | null
  version: number
}

/**
 * Agent creation input
 *
 * ⚠️  THREE-WAY BINDING: When creating an agent, ensure:
 *     - user_access_provider references a valid, enabled UserAccessProvider
 *     - ?? (not sure on this one) provider_type matches what that UserAccessProvider supports
 *     - ?? (not sure on this one) model_name is available at that UserAccessProvider
 *
 * Validation should happen:
 *     - Client-side: Before calling createAgent (via userAccessProviderService utilities)
 *     - Server-side: Backend validates these relationships (will return 400 if invalid)
 */
export interface CreateAgentInput {
  name: string
  slug: string
  description?: string | null
  model_name?: string
  provider_type?: string
  user_access_provider?: string | null  // UUID of UserAccessProvider entity
  system_prompt?: string | null
  participation_mode?: ParticipationMode
  scope?: AgentScope
  is_enabled?: boolean
}

/**
 * Agent update input
 */
export interface UpdateAgentInput {
  name?: string | null
  description?: string | null
  model_name?: string | null
  provider_type?: string | null
  user_access_provider?: string | null
  system_prompt?: string | null
  participation_mode?: ParticipationMode | null
  is_enabled?: boolean | null
}

/**
 * Paginated agents response
 */
export interface PaginatedAgents {
  agents: AgentViewModel[]
  total_count: number
}

interface GeneratedSlugResponse {
  slug: string
}

// ============================================================================
// Transformation Functions
// ============================================================================

/**
 * Transform backend UserAgentConfigPublic to AgentViewModel
 *
 * ⚠️  INPUT TYPE: Must be UserAgentConfigPublic (API response), NOT UserAgentConfigUpdate
 *
 * Transformations:
 * - Parses created_at, updated_at ISO strings to Date objects
 * - Provides defaults for nullable fields
 * - Casts scope and participation_mode to strict union types
 *
 * @param agent - Backend UserAgentConfigPublic (from API response)
 * @returns Transformed AgentViewModel optimized for UI
 */
function transformAgent(agent: UserAgentConfigPublic): AgentViewModel {
  return {
    id: agent.id,
    name: agent.name ?? "",
    slug: agent.slug ?? "",
    description: agent.description ?? null,

    // Model Configuration
    model_id: agent.model_id ?? null,
    model_name: agent.model_name ?? "gpt-4o-mini",
    provider_type: agent.provider_type ?? "openai",
    user_access_provider: agent.user_access_provider ?? null,

    // Prompts and Instructions
    system_prompt: agent.system_prompt ?? null,
    custom_system_prompt: agent.custom_system_prompt ?? null,
    instructions: agent.instructions ?? null,

    // Configuration
    tool_config: agent.tool_config ?? null,
    deps_config: agent.deps_config ?? null,
    agent_metadata: agent.agent_metadata ?? null,

    // Behavior Settings
    is_enabled: agent.is_enabled ?? true,
    is_clonable: agent.is_clonable ?? true,
    is_visible: agent.is_visible ?? true,
    scope: (agent.scope as AgentScope) ?? "personal",
    participation_mode:
      (agent.participation_mode as ParticipationMode) ?? "on_mention",
    is_coordinator: agent.is_coordinator ?? false,
    max_tool_iterations: agent.max_tool_iterations ?? null,
    capabilities: agent.capabilities ?? [],

    // Metadata
    owner_id: agent.owner_id ?? null,
    created_at: new Date(agent.created_at),
    updated_at: agent.updated_at ? new Date(agent.updated_at) : null,
    version: agent.version,
  }
}

