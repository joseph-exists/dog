/**
 * Agent Service - Data Integration Layer
 *
 * Purpose: Provide a clean, type-safe abstraction over the OpenAPI client
 * for agent-related operations. Transforms backend response models to
 * frontend ViewModels optimized for UI consumption.
 *
 * Architecture:
 * - Wraps OpenAPI client methods for UserAgentConfig CRUD
 * - Transforms backend UserAgentConfigPublic to AgentViewModel
 * - Normalizes nullable fields and provides UI-friendly defaults
 * - Centralizes API endpoint usage
 * - Provides type-safe interfaces with strict union types
 *
 * Note: This service manages UserAgentConfig entities (configuration templates),
 * NOT runtime Agent processes. AgentViewModel represents the config used to
 * instantiate Agents, not the Agents themselves.
 *
 * Dependencies:
 * - OpenAPI auto-generated client (@/client)
 * - llmCatalogService (for LLMProviderType type)
 */

// REMEMBER: a UserAgentConfig creates a construct that is used to generate an Agent.
// An Agent is a process that uses that config, and constructs the message it passes to the API.
// The way that message is constructed is dependent on the ProviderType - will the agent generated by this config need to talk 'openai' or will it need to talk 'anthropic' or something else?
// the useraccessprovider is how the user specifies the channel that the conversation will happen over -
// for the conversation to happen between our system and the main openai endpoint
// requires that the user have a useraccessprovider set with a) the openai endpoint and b) their sdk key to access that openai endpoint.
// for a UserAgentConfig to be used by the Agent generation process, it needs an association with
// a) a valid useraccessprovider,
// b) a model that is supported by that specific useraccessprovider -> for their account with that access provider, and c) an association with a model that is valid for
// c) a provider type that is valid for that useraccessprovider and that model.
//  if any of these are not aligned, then the Agent generated will not be successful in the work it attempts.
//  if the UserAgentConfig does not have a valid useraccessprovider, then there is no valid API for the Agent to connect to.
//  if the UserAgentConfig does not have a valid providertype:useraccessprovider, then the Agent generated from that UserAgentConfig will send messages to the API that the API can't process.
//  if the UserAgentConfig does not have a valid providertype:model association, then the Agent generated from that UserAgentConfig will send messages to the API that the model serving that endpoint can't process.
//  if the UserAgentConfig does not have a valid useraccessprovider:model association, then the Agent generated from that UserAgentConfig will send messages that are rejected by the API because that endpoint doesn't serve that model.

// ARCHITECTURE SUMMARY - Three-Way Binding:
//
// UserAgentConfig stores THREE critical associations that must align:
//   1. user_access_provider (UUID) → Which UserAccessProvider entity to use
//   2. provider_type (string)      → Which API specification format (openai, anthropic, etc.)
//   3. model_name (string)         → Which model to request
//
// When an Agent is instantiated from a UserAgentConfig:
//   - user_access_provider provides: base_url, api_key (WHERE and WITH WHAT CREDENTIALS)
//   - provider_type provides: message format, API schema (HOW to structure requests)
//   - model_name provides: which model to invoke (WHAT to talk to)
//
// All three must be mutually compatible or the Agent will fail at runtime.
// Backend performs authoritative validation, but client-side validation improves UX.
// See userAccessProviderService for validation utilities.

